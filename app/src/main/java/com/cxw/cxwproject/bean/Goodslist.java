package com.cxw.cxwproject.bean;

public class Goodslist {
	private String goodsId;// ":商品id
	private String price;// ": 98,
	private String flowerLanguage;// 花语
	private String name;// ": "小太阳",
	private String goodsImage;// 图片
	private String productId;// "货品id，下单使用

	public String getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getFlowerLanguage() {
		return flowerLanguage;
	}

	public void setFlowerLanguage(String flowerLanguage) {
		this.flowerLanguage = flowerLanguage;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGoodsImage() {
		return goodsImage;
	}

	public void setGoodsImage(String goodsImage) {
		this.goodsImage = goodsImage;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

}
